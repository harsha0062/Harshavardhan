no=[1,2,3,4,5]
no.append(6) # it is used to insert element in list at end only
print(no)
no.insert(2,0) # it is also used to insert element in list at particular position
print(no)
no.remove(0) # it is used to delete the element from the list (what the element is to be delete from list )
print(no)
no.clear() # it is used to delete the complete list 
print(no)
print(1 in no) # it verify whether the element is there in that list or not 
print(len(no)) # it is used to find the length of list 